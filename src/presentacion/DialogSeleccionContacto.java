    void actualizarMdlContactos(ArrayList<Contacto> contactos) {
        this.mdlContactos.removeAllElements();
        for(Contacto c : contactos)
        {
            this.mdlContactos.addElement(c);
        }
        this.cmbContactos.repaint();
    }
